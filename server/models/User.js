const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const userSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    phoneNumber: {
        type: String,
        required: true
    },
    address: {
        type : mongoose.Schema.Types.ObjectId,
        ref : 'Address'
    }
});

const User = mongoose.model('User', userSchema);

module.exports = User;
